    private void loaddata() {
        try {
            try {
                ILopHocDAO lopHocDAO = (ILopHocDAO) Class.forName("LopHoc.LopHocDAO").newInstance();
                allLop = new LopHocDAO().getAll();
                for (LopHoc lh : allLop) {
                    dtm.addRow(toObjectsData(lh));
                }
                jtbLop.setModel(dtm);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(frmLopHoc.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (InstantiationException ex) {
            Logger.getLogger(frmLopHoc.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalAccessException ex) {
            Logger.getLogger(frmLopHoc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private static Object[] toObjectsData(LopHoc lh) {
        Object[] objectsdata = {lh.getMalop(), lh.getTenlop(), lh.getMakhoa(), lh.getKhoahoc()};
        return objectsdata;
    }

    public boolean checkinfo() {
        Check c = new Check();
        if (!c.checkID(jtfMaLop.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập mã lớp sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfMaLop.setText("");
            jtfMaLop.requestFocus();
            return false;
        } else if (!c.checkSpace(jtfTenLop.getText()) || !c.check(jtfTenLop.getText())) {
            JOptionPane.showMessageDialog(this, "Nhập tên lơp học sai", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfTenLop.setText("");
            jtfTenLop.requestFocus();
            return false;

        } else if (!c.checkSpace(jtfKhoaHoc.getText()) || !c.check(jtfKhoaHoc.getText())) {
            JOptionPane.showMessageDialog(this, "Lỗi khóa học", "Hãy nhập lại", JOptionPane.ERROR_MESSAGE);
            jtfKhoaHoc.setText("");
            jtfKhoaHoc.requestFocus();
            return false;
        }
        return true;
    }
