    private void showAll() {
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        int selectedIndex = jcbMaKhoa.getSelectedIndex();
        Khoa getkhoa = allKhoa.get(selectedIndex);
        ArrayList<LopHoc> lhs = new LopHocDAO().findByIDKhoa(getkhoa.getMakhoa());
        for (LopHoc lopHoc : lhs) {
            Vector v = new Vector();
            v.add(lopHoc.getMalop());
            v.add(lopHoc.getTenlop());
            v.add(lopHoc.getMakhoa());
            v.add(lopHoc.getKhoahoc());
            dtm.addRow(v);
        }
        ResetForm();
    }

